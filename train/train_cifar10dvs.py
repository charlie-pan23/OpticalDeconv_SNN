def build_dataset(config: Dict[str, Any]) -> CIFAR10DVS:
    """Build CIFAR10-DVS frame dataset using the frozen preprocessed cache."""
    ds_cfg = config["dataset"]
    return CIFAR10DVS(
        root=config["paths"]["dataset_root"],
        data_type=ds_cfg.get("data_type", "frame"),
        frames_number=int(ds_cfg["time_steps"]),
        split_by=ds_cfg.get("split_by", "number"),
    )


def _strip_module_prefix(state_dict: Dict[str, torch.Tensor]) -> Dict[str, torch.Tensor]:
    """Remove DataParallel/DDP 'module.' prefix when needed."""
    cleaned = {}
    for key, value in state_dict.items():
        new_key = key[7:] if key.startswith("module.") else key
        cleaned[new_key] = value
    return cleaned


def _extract_model_state(ckpt: Dict[str, Any]) -> Dict[str, torch.Tensor]:
    """Extract model state dict from common checkpoint formats."""
    for key in ["model", "model_state_dict", "state_dict", "net"]:
        value = ckpt.get(key)
        if isinstance(value, dict):
            return _strip_module_prefix(value)

    # Some files are directly saved as a raw model state_dict.
    # In that case, all values should be tensors or tensor-like objects.
    if all(torch.is_tensor(v) for v in ckpt.values()):
        return _strip_module_prefix(ckpt)  # type: ignore[arg-type]

    raise KeyError(
        "Could not find model weights in checkpoint. Expected one of: "
        "'model', 'model_state_dict', 'state_dict', 'net', or a raw state_dict."
    )


def load_training_checkpoint(
    checkpoint_path: str | Path,
    model: torch.nn.Module,
    optimizer: torch.optim.Optimizer,
    scheduler: Any,
    device: torch.device,
) -> Tuple[int, float, int]:
    """Resume model, optimizer, scheduler, start epoch, and best-val metadata.

    Returns:
        start_epoch: next epoch to run
        best_val: best validation accuracy stored in checkpoint metadata
        best_epoch: epoch where best validation accuracy was obtained
    """
    checkpoint_path = Path(checkpoint_path)
    ckpt = torch.load(checkpoint_path, map_location=device, weights_only=False)

    if not isinstance(ckpt, dict):
        raise TypeError(f"Unsupported checkpoint format from {checkpoint_path}: {type(ckpt)}")

    model_state = _extract_model_state(ckpt)
    missing, unexpected = model.load_state_dict(model_state, strict=False)
    if missing:
        logger.warning(f"Missing keys when resuming model: {missing}")
    if unexpected:
        logger.warning(f"Unexpected keys when resuming model: {unexpected}")

    # Optimizer state: support both our naming and common naming.
    # Optimizer state: support both our naming and common naming.
    opt_state = ckpt.get("optimizer")
    if opt_state is None:
        opt_state = ckpt.get("optimizer_state_dict")

    if isinstance(opt_state, dict):
        optimizer.load_state_dict(opt_state)
    elif opt_state is not None:
        logger.warning(
            f"Optimizer state found but has unsupported type {type(opt_state)}; "
            "optimizer starts from current config."
        )
    else:
        logger.warning("No optimizer state found in checkpoint; optimizer starts from current config.")

    # Scheduler state: support both our naming and common naming.
    sched_state = ckpt.get("scheduler")
    if sched_state is None:
        sched_state = ckpt.get("scheduler_state_dict")

    if scheduler is not None and isinstance(sched_state, dict):
        scheduler.load_state_dict(sched_state)
    elif scheduler is not None and sched_state is not None:
        logger.warning(
            f"Scheduler state found but has unsupported type {type(sched_state)}; "
            "scheduler starts from current config."
        )
    elif scheduler is not None:
        logger.warning("No scheduler state found in checkpoint; scheduler starts from current config.")

    start_epoch = int(ckpt.get("epoch", 0)) + 1

    metrics = ckpt.get("metrics", {})
    best_val = -1.0
    best_epoch = -1

    if isinstance(metrics, dict):
        # Newer checkpoints from this script save these fields directly.
        if "best_val" in metrics:
            best_val = float(metrics["best_val"])
        elif "best_val_acc" in metrics:
            best_val = float(metrics["best_val_acc"])

        if "best_epoch" in metrics:
            best_epoch = int(metrics["best_epoch"])

        # Fallback: if no best metadata exists, use the current validation acc.
        if best_val < 0 and isinstance(metrics.get("val"), dict):
            best_val = float(metrics["val"].get("acc", -1.0))
            best_epoch = int(ckpt.get("epoch", -1))

    # Some save_checkpoint implementations may store metadata at top level.
    if best_val < 0:
        if "best_val" in ckpt:
            best_val = float(ckpt["best_val"])
        elif "best_val_acc" in ckpt:
            best_val = float(ckpt["best_val_acc"])

    if best_epoch < 0 and "best_epoch" in ckpt:
        best_epoch = int(ckpt["best_epoch"])

    # If still unknown, do not block training; restart best tracking.
    return start_epoch, best_val, best_epoch


def configure_cuda_runtime(config: Dict[str, Any], device: torch.device) -> None:
    """Enable runtime switches useful for fixed-shape CUDA training."""
    if device.type != "cuda":
        return

    exp = config.get("experiment", {})
    torch.backends.cudnn.benchmark = bool(exp.get("benchmark", True))
    torch.backends.cudnn.deterministic = bool(exp.get("deterministic", False))
    torch.backends.cuda.matmul.allow_tf32 = True
    torch.backends.cudnn.allow_tf32 = True

    try:
        torch.set_float32_matmul_precision("high")
    except Exception:
        pass


def main() -> None:
    parser = argparse.ArgumentParser(description="Train CIFAR10-DVS SNN baseline.")
    parser.add_argument("--config", type=str, default="configs/config_cifar10dvs.yaml")
    parser.add_argument("--device", type=str, default="auto")

    # Explicit meanings:
    # --resume restores full training state; --weights only loads model weights.
    parser.add_argument("--resume", type=str, default=None, help="Resume training from a full training checkpoint.")
    parser.add_argument("--weights", type=str, default=None, help="Load model weights only before training.")
    parser.add_argument("--checkpoint", type=str, default=None, help="Checkpoint path for test-only/eval-only.")
    parser.add_argument("--test-only", action="store_true", help="Evaluate the checkpoint on the test split only.")
    parser.add_argument("--eval-only", action="store_true", help="Alias of --test-only.")
    args = parser.parse_args()

    if args.resume and args.weights and not (args.test_only or args.eval_only):
        raise ValueError("Use only one of --resume or --weights during training.")

    config = load_config(args.config)
    exp = config.get("experiment", {})
    set_seed(
        int(exp.get("seed", 42)),
        bool(exp.get("deterministic", True)),
        bool(exp.get("benchmark", False)),
    )
    device = choose_device(args.device)
    configure_cuda_runtime(config, device)

    split_file = Path(config["split"]["split_file"])
    if not split_file.exists():
        raise FileNotFoundError(f"Split file not found: {split_file}. Run train/pre_cifar10dvs.py first.")
    split = load_split(split_file)

    dataset = build_dataset(config)
    train_ds = Subset(dataset, split["train_indices"])
    val_ds = Subset(dataset, split["val_indices"])
    test_ds = Subset(dataset, split["test_indices"])

    tr = config["training"]
    train_loader = build_loader(
        train_ds,
        batch_size=int(tr["batch_size_train"]),
        shuffle=True,
        config=config,
        drop_last=bool(tr.get("train_drop_last", True)),
    )
    val_loader = build_loader(
        val_ds,
        batch_size=int(tr["batch_size_eval"]),
        shuffle=False,
        config=config,
        drop_last=bool(tr.get("eval_drop_last", False)),
    )
    test_loader = build_loader(
        test_ds,
        batch_size=int(tr["batch_size_eval"]),
        shuffle=False,
        config=config,
        drop_last=bool(tr.get("eval_drop_last", False)),
    )

    run_dir = create_run_dir(config, args.config, "cifar10dvs")

    save_json(
        {
            "split_file": str(split_file),
            "split_summary": {"train": len(train_ds), "val": len(val_ds), "test": len(test_ds)},
        },
        run_dir / "metrics" / "split_used.json",
    )
    save_json(config, run_dir / "metrics" / "config_used.json")
    save_json(vars(args), run_dir / "metrics" / "args_used.json")

    model = build_model(config).to(device)
    model_info = describe_model(model)
    save_json(model_info, run_dir / "metrics" / "model_info.json")

    tqdm.write("=== CIFAR10-DVS training ===")
    tqdm.write(f"Run dir: {run_dir}")
    tqdm.write(f"Device: {device}")
    tqdm.write(f"Model: {model_info}")
    tqdm.write(f"Train/Val/Test samples: {len(train_ds)} / {len(val_ds)} / {len(test_ds)}")

    logger.info("=== CIFAR10-DVS training ===")
    logger.info(f"Run dir: {run_dir}")
    logger.info(f"Device: {device}")
    logger.info(f"Model: {model_info}")
    logger.info(f"Train/Val/Test samples: {len(train_ds)} / {len(val_ds)} / {len(test_ds)}")

    optimizer = build_optimizer(model, config)
    scheduler = build_scheduler(optimizer, config)
    criterion = build_criterion(config)
    scaler = get_grad_scaler(bool(tr.get("amp", False)))

    # Test-only mode: do not train and do not rely on the newly created run_dir checkpoint.
    if args.test_only or args.eval_only:
        ckpt = args.checkpoint or args.weights or args.resume
        if ckpt is None:
            raise ValueError(
                "Test-only mode requires --checkpoint, --weights, or --resume.\n"
                "Example:\n"
                "  python train/train_cifar10dvs.py "
                "--config configs/config_cifar10dvs.yaml "
                "--test-only "
                "--checkpoint results/cifar10dvs/run_xxx/checkpoints/best_val.pth"
            )

        tqdm.write(f"Testing checkpoint: {ckpt}")
        logger.info(f"Testing checkpoint: {ckpt}")
        load_model_weights(model, ckpt, device)

        test_metrics = evaluate(model, test_loader, criterion, config, device, desc="Test")
        result = {
            "checkpoint": str(ckpt),
            "split_file": str(split_file),
            "config_path": str(args.config),
            "test": test_metrics,
            "run_dir": str(run_dir),
        }

        tqdm.write(f"Test metrics: {test_metrics}")
        logger.info(f"Test metrics: {test_metrics}")
        save_json(result, run_dir / "metrics" / "test_only.json")
        return

    # Load model weights only for fine-tuning / restart-from-weights.
    if args.weights:
        tqdm.write(f"Loading model weights: {args.weights}")
        logger.info(f"Loading model weights: {args.weights}")
        load_model_weights(model, args.weights, device)

    best_val = -1.0
    best_epoch = -1
    start_epoch = 1

    # Resume full training state after optimizer/scheduler are created.
    if args.resume:
        tqdm.write(f"Resuming full checkpoint: {args.resume}")
        logger.info(f"Resuming full checkpoint: {args.resume}")
        start_epoch, best_val, best_epoch = load_training_checkpoint(
            args.resume,
            model,
            optimizer,
            scheduler,
            device,
        )
        tqdm.write(f"Resumed from epoch {start_epoch - 1}, best={best_val:.2f}%@{best_epoch:03d}")
        logger.info(f"Resumed from epoch {start_epoch - 1}, best={best_val:.2f}%@{best_epoch:03d}")

    epochs = int(tr["epochs"])
    save_interval = int(tr.get("checkpoint", {}).get("save_interval_epochs", 10))
    train_log = run_dir / "metrics" / "train_log.csv"

    # Early stopping config.
    early_cfg = tr.get("early_stopping", {})
    early_enabled = bool(early_cfg.get("enabled", True))
    early_patience = int(early_cfg.get("patience", 15))
    early_min_epochs = int(early_cfg.get("min_epochs", 50))
    early_min_delta = float(early_cfg.get("min_delta", 0.2))

    epochs_without_improve = 0
    # early_best_val controls patience. best_val controls checkpoint selection.
    early_best_val = best_val

    last_epoch_completed = start_epoch - 1

    try:
        for epoch in range(start_epoch, epochs + 1):
            last_epoch_completed = epoch
            lr_used = float(optimizer.param_groups[0]["lr"])

            train_metrics = train_one_epoch(
                model,
                train_loader,
                optimizer,
                criterion,
                config,
                device,
                scaler,
                epoch,
            )
            val_metrics = evaluate(
                model,
                val_loader,
                criterion,
                config,
                device,
                desc=f"Val {epoch}",
            )

            current_val = float(val_metrics["acc"])

            # Save best_val.pth for any real improvement.
            is_best = current_val > best_val if best_epoch >= 0 else True
            if is_best:
                best_val = current_val
                best_epoch = epoch

            # For early stopping, require a meaningful improvement.
            significant_improve = current_val > (early_best_val + early_min_delta) if early_best_val >= 0 else True
            if significant_improve:
                early_best_val = current_val
                epochs_without_improve = 0
            else:
                epochs_without_improve += 1

            row = {
                "epoch": epoch,
                "lr": lr_used,
                "train_loss": train_metrics["loss"],
                "train_acc": train_metrics["acc"],
                "val_loss": val_metrics["loss"],
                "val_acc": val_metrics["acc"],
                "best_val_acc": best_val,
                "best_epoch": best_epoch,
                "is_best": int(is_best),
                "early_best_val": early_best_val,
                "epochs_without_improve": epochs_without_improve,
            }
            append_csv_row(train_log, row)

            msg = (
                f"Epoch {epoch:03d}/{epochs} | LR {lr_used:.6g} | "
                f"Train {train_metrics['acc']:.2f}% loss {train_metrics['loss']:.4f} | "
                f"Val {val_metrics['acc']:.2f}% loss {val_metrics['loss']:.4f} | "
                f"Best {best_val:.2f}%@{best_epoch:03d}"
            )

            tqdm.write(msg)
            logger.info(msg)

            # Step scheduler after this epoch. The checkpoint then stores the state
            # that should be used by the next epoch if training is resumed.
            if scheduler is not None:
                scheduler.step()

            checkpoint_metrics = {
                "train": train_metrics,
                "val": val_metrics,
                "best_val": best_val,
                "best_epoch": best_epoch,
                "early_best_val": early_best_val,
                "epochs_without_improve": epochs_without_improve,
            }

            # Save checkpoints before early-stopping break.
            save_checkpoint(
                run_dir / "checkpoints" / "last.pth",
                model,
                optimizer,
                scheduler,
                epoch,
                checkpoint_metrics,
                config,
            )

            if epoch % save_interval == 0:
                save_checkpoint(
                    run_dir / "checkpoints" / f"epoch_{epoch:03d}.pth",
                    model,
                    optimizer,
                    scheduler,
                    epoch,
                    checkpoint_metrics,
                    config,
                )

            if is_best:
                save_checkpoint(
                    run_dir / "checkpoints" / "best_val.pth",
                    model,
                    optimizer,
                    scheduler,
                    epoch,
                    checkpoint_metrics,
                    config,
                )
                logger.info(f"New best val checkpoint: epoch {epoch}, val_acc={best_val:.2f}%")

            if (
                early_enabled
                and epoch >= early_min_epochs
                and epochs_without_improve >= early_patience
            ):
                stop_msg = (
                    f"Early stopping at epoch {epoch}: "
                    f"best_val={best_val:.2f}%@{best_epoch:03d}, "
                    f"no significant improvement > {early_min_delta:.2f}% "
                    f"for {epochs_without_improve} epochs."
                )
                tqdm.write(stop_msg)
                logger.info(stop_msg)
                break

    except KeyboardInterrupt:
        tqdm.write("Training interrupted by user. Saving interrupted checkpoint...")
        logger.warning("Training interrupted by user. Saving interrupted checkpoint...")
        save_checkpoint(
            run_dir / "checkpoints" / "interrupted.pth",
            model,
            optimizer,
            scheduler,
            last_epoch_completed,
            {
                "best_val": best_val,
                "best_epoch": best_epoch,
                "early_best_val": early_best_val,
                "epochs_without_improve": epochs_without_improve,
            },
            config,
        )
        raise

    # Final test is run once using best validation checkpoint.
    best_path = run_dir / "checkpoints" / "best_val.pth"
    if not best_path.exists():
        raise FileNotFoundError(
            f"Best validation checkpoint not found: {best_path}. "
            "Training may have failed before the first validation completed."
        )

    load_model_weights(model, best_path, device)
    test_metrics = evaluate(model, test_loader, criterion, config, device, desc="Final Test")

    final = {
        "best_epoch": best_epoch,
        "best_val_acc": best_val,
        "best_checkpoint": str(best_path),
        "split_file": str(split_file),
        "config_path": str(args.config),
        "test": test_metrics,
        "run_dir": str(run_dir),
    }
    save_json(final, run_dir / "metrics" / "final_test.json")

    tqdm.write(f"Final test with best-val checkpoint: {test_metrics}")
    tqdm.write("=== CIFAR10-DVS training complete ===")

    logger.info(f"Final test with best-val checkpoint: {test_metrics}")
    logger.info("=== CIFAR10-DVS training complete ===")


if __name__ == "__main__":
    main()
