"""Train CIFAR10-DVS SNN baseline for HIPSA.

This script is intentionally tied to the split generated by pre_cifar10dvs.py.
It does not create a new split during training, which keeps eval00-03 aligned
with the trained checkpoint.

Recommended usage from project root:
  python train/pre_cifar10dvs.py --config configs/config_cifar10dvs.yaml

  # Train
  python train/train_cifar10dvs.py --config configs/config_cifar10dvs.yaml

  # Test an existing best-val checkpoint
  python train/train_cifar10dvs.py \
    --config configs/config_cifar10dvs.yaml \
    --test-only \
    --checkpoint results/cifar10dvs/run_xxx/checkpoints/best_val.pth

  # Resume a full training checkpoint
  python train/train_cifar10dvs.py \
    --config configs/config_cifar10dvs.yaml \
    --resume results/cifar10dvs/run_xxx/checkpoints/last.pth
"""

